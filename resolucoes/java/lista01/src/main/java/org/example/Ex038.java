/*
Escreva um programa que mostre na tela a seguinte contagem:
    6 7 8 9 10 11 Acabou!
*/
package org.example;
public class Ex038
{  
    public static void main(String[] args){
        Ex038.main();
    }
    public static void main(){
        for(int i = 6; i <= 11; i++){
            System.out.print(i + " ");
        }
        System.out.println("Acabou!");
    }
    
    
}
